package day03_EscapeSequences;
/*
Escape sequences: MUST be given with double quate
\n: starts a new line
\t: paragraph space (tab)
\\: one back slash
\" : double quote


 */



public class EscapeSequencess {

    public static void main(String[] args) {
        System.out.println("Java \n Python \n C#");
        System.out.println("Hello Cydeo \nHow are you all ? \nhow feel today? \nit's nice to see you all!");
        System.out.println("--------------------");
        System.out.println("\tJava is cool programming Language");
        System.out.println("------------------");
        System.out.println("/ \\");
        System.out.println(" \\ \\");
        System.out.println("----------------");
        System.out.println("my favorite tv show \"Game Of Thrones\"");



    }

}
