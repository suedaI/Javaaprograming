package day15_ForLoop;

public class ForLoopPractices {
    public static void main(String[] args) {

        for (int i = 15; i <= 45; i++) { //i:15,15,17...
            System.out.print(i + " ");
        }
        System.out.println();
        System.out.println("hello ");
        System.out.println("==============================");


        for (int i = 100; i >= 49; i--) { //i:100,99,98,...50
            System.out.print(i + " ");






        }







    }
}
